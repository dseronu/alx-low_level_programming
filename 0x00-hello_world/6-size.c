#include<stdio.h>

/**
 * main - main entry block
 * Return: always 0
 */

int main(void)
{
	printf("The size of a character: %d byte(s)\n", sizeof(char));
	printf("The size of an integer: %d byte(s)\n", sizeof(int));
	printf("The size of a float: %d byte(s)\n", sizeof(float));
	printf("The size of a double data type : %d byte(s)\n", sizeof(double));
	return (0);
}
